def clear_terminal() -> None:
    """
    clear the terminal
    """
    if os.name == "posix": # Unix
        os.system("clear")
    else: # Windows
        os.system("cls")


def show_title(debug_mode: bool = False):
    clear_terminal()
    print(SCREEN, DEBUG_SCREEN if debug_mode else "")
    time.sleep(2 if debug_mode else 5)
    clear_terminal()

if __name__ == "__main__":
    show_title(True)